#include "libft.h"

int	ft_atoi(const char *s)
{
	int			zn;
	long long	rez;

	zn = 1;
	rez = 0;
	while ((*s >= 9 && *s <= 13) || *s == 32)
		s++;
	if (*s == '-')
	{
		zn = -1;
		s++;
	}
	else if (*s == '+')
		s++;
	while (*s >= '0' && *s <= '9')
	{
		if ((rez * zn) > 922337203685477580)
			return (-1);
		if ((rez * zn) < -922337203685477580)
			return (0);
		rez = rez * 10 + (*s - '0');
		s++;
	}
	return (int)(rez * zn);
}
