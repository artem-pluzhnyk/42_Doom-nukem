#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list *nxtlst;

	if (!alst || !del)
		return ;
	while (*alst)
	{
		nxtlst = (*alst)->next;
		del((*alst)->content, (*alst)->content_size);
		free(*alst);
		*alst = nxtlst;
	}
	*alst = NULL;
}
