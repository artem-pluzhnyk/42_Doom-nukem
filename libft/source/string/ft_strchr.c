#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*str;

	i = -1;
	str = (char *)s;
	while (str[++i])
	{
		if (str[i] == (char)c)
			return (str + i);
	}
	if (str[i] == (char)c)
		return (str + i);
	return (NULL);
}
