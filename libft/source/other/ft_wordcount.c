#include "libft.h"

int		ft_wordcount(const char *s, char c)
{
	int n;
	int i;

	n = 0;
	i = 0;
	while (s[i])
	{
		if (s[i] == c && s[i + 1 != c])
			n++;
		i++;
	}
	n++;
	return (n);
}
